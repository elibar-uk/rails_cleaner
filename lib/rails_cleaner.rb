module RailsCleaner
  def self.hello
    puts "Hello world"
  end

  def self.delete(file_path)
    File.delete(file_path)
  end

  def self.delete_from(text_file)
    file_names = ""
    File.open(text_file).each do |line|
      file_names << line
    end
      # file_names.gsub("'", "").split(", ")
    file_names.gsub("'", "").split(", ").each do |file_name|
      RailsCleaner.delete(file_name)
    end
  end

  # useful methods for later:
